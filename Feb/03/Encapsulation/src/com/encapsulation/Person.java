package com.encapsulation;

public class Person {

    private String name;
    private int age;

    public void setName(String value)
    {
        name = value;
    }

    public String getName()
    {
        return name;
    }

    public void setAge(int value)
    {
        age = value;
    }

    public int getAge()
    {
        return age;
    }

}
